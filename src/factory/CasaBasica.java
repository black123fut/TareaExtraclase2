package factory;

public class CasaBasica {
	public int NumeroCuartos;
	public int NumeroBath;
	public boolean TienePatio;
	public int CantidadPisos;
	public String Area;
	
	public int getNumeroCuartos() {
		return NumeroCuartos;
	}
	public int getNumeroBath() {
		return NumeroBath;
	}
	public boolean isTienePatio() {
		return TienePatio;
	}
	public int getCantidadPisos() {
		return CantidadPisos;
	}
	public String getArea() {
		return Area;
	}
	
	
}
